// Data Type Checking

/* ======== Numeric/Number Type Data :: Check by 'typeof' ======== */
var price = 100;
console.log(typeof price);


/* ======== String Type Data :: Check by 'typeof' ======== */
var newPrice = "100";
console.log(typeof newPrice);


/* ======== Boolean Type Data :: Check by 'typeof' ======== */
var isHappy = true;
console.log(typeof isHappy);


/* ======== Undefined Type Data :: Check by 'typeof' ======== */
var romantic;
console.log(typeof romantic);